/**
 * @param {number} x
 */
function dsp_is_power_of_two(x) {
  return x != 0 && (x & (x - 1)) == 0;
}

/** @type {number} @constant */
var add_rount_mult = 0x7fff;
/** @type {number} @constant */
var mult_shift_const = 0x7fff; // Used to shift data << 15

/**
 * @param {number[]} data
 * @param {number[]} sc_table
 */
function dsps_fft2r_sc16_ansi_(data, sc_table) {
  if (!dsp_is_power_of_two(data.length)) {
    return true;
  }

  var /** @type {number} */ ie,
    /** @type {number} */ ia,
    /** @type {number} */ m;

  /** @type {sc16_t} */
  var cs = { re: 0, im: 0 }; // c - re, s - im
  /** @type {sc16_t} */
  var m_data = { re: 0, im: 0 };
  /** @type {sc16_t} */
  var a_data = { re: 0, im: 0 };

  ie = 1;
  for (var N2 = data.length / 4; N2 > 0; N2 = N2 >> 1) {
    ia = 0;
    for (var j = 0; j < ie; j++) {
      cs.re = sc_table[2 * j];
      cs.im = sc_table[2 * j + 1];

      for (var i = 0; i < N2; i++) {
        m = ia + N2;

        m_data.re = data[2 * m];
        m_data.im = data[2 * m + 1];

        a_data.re = data[2 * ia];
        a_data.im = data[2 * ia + 1];

        /** @type {sc16_t} */
        var m1 = { re: 0, im: 0 };
        /** @type {number} */
        var result = a_data.re * mult_shift_const;
        result -= cs.re * m_data.re + cs.im * m_data.im;
        result += add_rount_mult;
        result = result >> 16;
        m1.re = result;

        result = a_data.im * mult_shift_const;
        result -= cs.re * m_data.im - cs.im * m_data.re;
        result += add_rount_mult;
        result = result >> 16;
        m1.im = result;

        data[m * 2] = m1.re;
        data[m * 2 + 1] = m1.im;

        /** @type {sc16_t} */
        var m2 = { re: 0, im: 0 };
        result = a_data.re * mult_shift_const;
        result += cs.re * m_data.re + cs.im * m_data.im;
        result += add_rount_mult;
        result = result >> 16;
        m2.re = result;

        result = a_data.im * mult_shift_const;
        result += cs.re * m_data.im - cs.im * m_data.re;
        result += add_rount_mult;
        result = result >> 16;
        m2.im = result;
        data[ia * 2] = m2.re;
        data[ia * 2 + 1] = m2.im;

        ia++;
      }
      ia += N2;
    }
    ie = ie << 1;
  }
  return false;
}

